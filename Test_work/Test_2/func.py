def luc_tickets(num_1, num_2):
    """
    Функція яка приймає два цілі невід'ємні числа num_1, num_2
    та повертає кількість счастливих квитків в діапазоні від num_1 до num_2
    Якщо число на проміжку має меньше 6 цифр то на його початку дописуються нулі
    """
    
    count = 0  # Лічильник щасливих квитків
    
    for num in range(num_1, num_2 + 1):
        # Перевіряємо чи число має 6 цифр
        if len(str(num)) < 6:
            num = str(num).zfill(6)  # Додаємо нулі на початку числа
        else:
            num = str(num)
        # Перевіряємо чи число щасливе
        if sum(map(int, num[:3])) == sum(map(int, num[3:])):
            count += 1  # Збільшуємо лічильник щасливих квитків
    return count  # Повертаємо кількість щасливих квитків
    
    
def song(x, y, z):
    """
    Функція яка приймає три цілі невід'ємні числа x, y, z
    та повертає рядок "Ewerbody sing a song: текст пісні."
    де текст формується з y куплетів розділених пробілами всі куплети однакові і складаються з x "la"
    через дефіс 
    якщо z = 1 то в кінці куплету додається "!"
    якщо z = 0 то в кінці куплету додається "."
    за відсутності куплетів пробіл перед крапкою окличним знаком не ставиться
    """
    
    if x <= 0 or y < 0 or (z != 0 and z != 1):   # Перевіряємо чи числа не від'ємні і не нульові
        print ("Невірні числа")
        return ''
    
    if y == 0:   # Якщо немає куплетів
        return f"Everybody sing a song:{'!' if z == 1 else '.'}"

    string = '-'.join(['la'] * x)  # Формуємо рядок з куплетами
    song = ' '.join([string] * y)  # Формуємо рядок з куплетами
    return f"Everybody sing a song: {song}{'!' if z == 1 else '.'}"  # Повертаємо рядок з піснею

